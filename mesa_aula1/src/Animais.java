public class Animais {
    private String tipo;
    private String nome;
    private int idade;
    private String qtdComida;
    private String barulho;

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public String getQtdComida() {
        return qtdComida;
    }

    public void setQtdComida(String qtdComida) {
        this.qtdComida = qtdComida;
    }

    public String getBarulho() {
        return barulho;
    }

    public void setBarulho(String barulho) {
        this.barulho = barulho;
    }
}
